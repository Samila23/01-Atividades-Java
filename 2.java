//2 -Faça um programa para ler o valor do raio de um círculo, e depois mostrar o valor da área deste círculo com quatro casas decimais conforme exemplos.
//Fórmula da área: area = π . raio2
//Considere o valor de π = 3.14159


import java.util.Locale;
import java.util.Scanner;

public class Main {

	public static void main(String[] args) {
		Locale.setDefault(Locale.US);
		Scanner sc = new Scanner (System.in);

		double pi = 3.14159 , a , area, raio;

		System.out.println("Para calcular o valor da area , digite o valor do raio");
		a = sc.nextDouble();
		raio = (a*a);
		area = (pi * raio);
		System.out.printf("O valor da area é %.4f%n", area);

		sc.close();	
	}

}
